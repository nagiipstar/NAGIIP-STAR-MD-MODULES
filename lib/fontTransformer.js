function loadFontSettings() {
    const settingsFile = path.join(__dirname, '../data/botFont.json');
    try {
        if (fs.existsSync(settingsFile)) {
            const data = fs.readFileSync(settingsFile, 'utf8');
            return JSON.parse(data);
        }
    } catch (error) {
        console.error('Error loading font settings:', error);
    }
    return { currentFont: 'default', enabled: true };
}

function wrapSendMessage(sock) {
    const originalSendMessage = sock.sendMessage.bind(sock);
    
    sock.sendMessage = async (jid, content, options = {}) => {
        try {
            const settings = loadFontSettings();
            
            if (settings.enabled && content.text && settings.currentFont && settings.currentFont !== 'default') {
                const style = fontStyles[settings.currentFont];
                if (style && style.transform) {
                    content.text = style.transform(content.text);
                }
            }
        } catch (error) {
            console.error('Error in font transformation:', error);
        }
        
        return originalSendMessage(jid, content, options);
    };
    
    console.log('âœ… Font transformation system activated');
}

